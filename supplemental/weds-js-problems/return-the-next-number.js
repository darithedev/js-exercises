/*  Create a function that takes a number as an argument, increments the number by +1 and returns the result.
    Examples:
    addition(0) ➞ 1

    addition(9) ➞ 10

    addition(-3) ➞ -2
    
    Notes:
    Don't forget to return the result.
*/

// Add function addOne (num) that takes a number as argument 
function addOne(num) {
    // within function add one to num and return 
    return num+=1;
}

console.log(addOne(0)) // 1
console.log(addOne(9)) // 10
console.log(addOne(-3)) // -2